/* ===== Apply language ===== */
function setLanguage(lang) {
  const dict = I18N[lang] || I18N.it;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] != null) el.textContent = dict[key];
  });
  try { localStorage.setItem("cb-lang", lang); } catch (e) {}
}

/* ===== Init ===== */
document.addEventListener("DOMContentLoaded", () => {
  // year
  const y = document.getElementById("year");
  if (y) y.textContent = new Date().getFullYear();

  // language
  let saved = "it";
  try { saved = localStorage.getItem("cb-lang") || "it"; } catch (e) {}
  setLanguage(saved);

  const langBtn = document.getElementById("langToggle");
  if (langBtn) {
    langBtn.addEventListener("click", () => {
      const next = document.documentElement.lang === "it" ? "en" : "it";
      setLanguage(next);
    });
  }

  // mobile nav
  const navToggle = document.getElementById("navToggle");
  const navLinks = document.getElementById("navLinks");
  if (navToggle && navLinks) {
    navToggle.addEventListener("click", () => {
      const open = navLinks.classList.toggle("open");
      navToggle.setAttribute("aria-expanded", open ? "true" : "false");
    });
    navLinks.querySelectorAll("a").forEach((a) =>
      a.addEventListener("click", () => {
        navLinks.classList.remove("open");
        navToggle.setAttribute("aria-expanded", "false");
      })
    );
  }

  // header shadow on scroll
  const header = document.querySelector(".site-header");
  const onScroll = () => header.classList.toggle("scrolled", window.scrollY > 8);
  onScroll();
  window.addEventListener("scroll", onScroll, { passive: true });

  // reveal + skill bars
  const io = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("in");
          entry.target.querySelectorAll?.(".bar").forEach((b) => b.classList.add("in"));
          io.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.12 }
  );
  document.querySelectorAll(".reveal").forEach((el) => io.observe(el));

  // safety: ensure everything is visible shortly after load
  window.addEventListener("load", () =>
    setTimeout(() => {
      document.querySelectorAll(".reveal:not(.in)").forEach((el) => el.classList.add("in"));
      document.querySelectorAll(".bar:not(.in)").forEach((b) => b.classList.add("in"));
    }, 1200)
  );
});
